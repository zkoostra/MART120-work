// Start the setup function
function setup() {

    // create a workspace that's 1200 pixels wide, 800 long
    createCanvas( 1200, 800 );

    // color the background turquoise
    background( 'turquoise' );
}


// Declare the draw function
function draw() {

    // tell p5 to write "Hello World!"
    // tell p5 to position Hello World at 600, 100 x,y
    text( "Hello World!", 600, 100 );
}
